#include<iostream>
using namespace std;

// Variables are containers used to store values 

int main() {

    //int type variable
    int number;
    number = 15; //asigned value

    //float
    float varFloat = 15.5; //value can be assigned while defining

    //charcter
    char myCharVar = 'D';

    //str
    string myStr = "Cpp";

    //const = value cannot be changed
    const float _pi = 3.14;

    // printing these variables
    cout<<number<<endl;
    cout<<varFloat<<endl;
    cout<<myCharVar<<endl;
    cout<<myStr<<endl;
    cout<<_pi<<endl;

}





    
    